      <font size="4">1. 종목설명</font>
    </b>
    <div
      class="indent-lv1"
    >무선 조종이 가능한 소형 주행로봇을 이용해 제한된 시간내에 상대방의 골대에 얼마나 많은 공을 넣는가를 겨루는 경기로 전략전술 및 순발력을 키우고, 팀웍을 놓일 수 있는 종목이다.</div>
