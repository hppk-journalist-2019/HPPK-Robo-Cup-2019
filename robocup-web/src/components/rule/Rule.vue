      <br />B. 공인구의 Start Point는 경기장 중앙으로 한다.
      <br />C. PK상황 이외에 공을 몰고 가는 행위를 허용한다.
      <br />D. 로봇 끼리의 접촉이나 진로방해에 의한 파울은 없다.
      <br />E. 공인구가 반짝일 때 득점 시 2점으로 간주한다.
      <br />F. 경기 도중 경기장 안에 모든 것(로봇, 공 포함 모든 시설)은 심판 이외에 터치 할 수 없다.
      <br />G. 경기 중단(Time stop) 상황은 아래와 같다.
      <br />
      <div class="indent-lv2">
        i. 로봇이 엉켜 있어 경기가 불가능한 경우
        <br />ii. 반칙 상황인 경우
        <br />iii. 골인 또는 공이 경기장 밖으로 나간 경우
        <br />iv. 그 외에 심판에 의해 경기 중단이 불가피한 경우 (이물질 및 로봇 고장 등)
        <br />
      </div>H. 경기 도중 로봇이 고장 난 경우 심판에 의해 경기 중단이 있을 때까지 경기를 지속한다.
      <br />I. 고장 난 로봇은 경기 중단 상황 및 휴식시간에 로봇 수리 가능하며, 경기 도중 투입 시 반드시 심판에게 전달한다.
      <br />J. 반칙 규정은 아래와 같다
      <br />
      <div class="indent-lv2">
        i. 경기 도중 경기장 안에 모든 것(로봇, 공 포함 모든 시설)에 대한 터치 행위 (1차 경고(팀), 2차 PK, 3차 퇴장)
