    <b>
      <font size="4">2. 팀구성</font>
    </b>
    <div
      class="indent-lv1"
    >총 16개팀(인원 10 ~ 12인)으로 한 팀에 같은 파트 3명 이상은 참여할 수 없다.</div>
    <br />
    <b>
      <font size="4">3. 경기방법</font>
    </b>
    <div class="indent-lv1">
      - 지정된 경기장에서 경기를 한다 .
      <br />- 모든 경기는 토너먼트 형식으로 진행된다 .
      <br />- 경기시간은 16강/8강 - 전후반 각3분(휴식 1분), 4강/결승 - 전후반 각5분(휴식 1분)으로 하며,정해진 시간 내에 골을 많이 넣는 팀이 승리한다.
      <br />- 무승부일 경우 연장 1분(골든골) 경기하며, 그래도 승패가 없을 시 승부차기 (3회 실시 후 동점시 1회 추가 )로 승패를 결정한다.
      <br />
    </div>
    <br />
    <b>
      <font size="4">4. 로봇규격</font>
    </b>
    <div class="indent-lv1">
      1) 경기에 참가하는 로봇은 미리 지급된 소형 주행로봇을 대상으로 한다.
      <br />2) 주행로봇은 다음과 같이 규격을 갖추어야 한다.
      <br />
      <div class="indent-lv2">
        A. 길이와 폭은 미리 지급된 로봇 키트에 들어갈 수 있는 사이즈여야 한다.
        <br />B. 높이에 제한은 없다.
        <br />C. 상대방을 파괴하거나 고장 나게 하는 형태의 무기나 구조를 사용할 수 없다.
        <br />D. 상대방과 몸싸움을 할 수 있는 수준의 구조 변경은 가능하다.
        <br />E. 공을 움직이지 못하게 잡을 수 있는 일체의 구조는 사용할 수 없다.
        <br />F. 모든 주행로봇은 경기 시뮬레이션 기간 (7/17 ~ 18) 내에 운영팀에게 안정성 검사를 받아야 한다.
        <br />G. 경기 시작 전 심판이 로봇을 점검하여, 로봇 규정 위반시 수정 전까지 출전 불가하다.
        <br />
      </div>
    </div>

